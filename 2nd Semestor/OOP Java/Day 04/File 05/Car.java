class Car{

	private String model;      // Private variable for encapsulation
	
	// Default constructor
	public Car() {

	}
	
	// Constructor with one parameter
	public Car(String model) {
		this.model = model;
	}
	
	// Getter method
	public String getmodel(){
			return model;
	}
	
	// Setter method
	public void setmodel(String Toyota){
		this.model=Toyota;
	}
	

}



